  <h3>SOBRE O DOCMARK</h3>
    <p>O DocMark é um software desenvolvido pela <a style="color: #fff"  href="https://backupcloud.site/" target="_blank">Backup Cloud</a> com o objetivo de atender aos requisitos do provimento 143/2023 do CNJ (Conselho Nacional de Justiça) e às recomendações da ONR para a criação de carimbo digital nas matrículas de imóveis.</p>
    <p>Com essa solução, é possível adicionar um carimbo digital no canto superior direito de todas as páginas de um arquivo PDF, garantindo a autenticidade, e atendendo o provimento 143/2023 do CNJ.</p>
    <p>O software é fácil de usar, permitindo que o usuário selecione o arquivo PDF desejado e insira o texto para o carimbo. Em seguida, o DocMark adiciona o carimbo digital em todas as páginas do PDF</p>
    <p>Desenvolvido com tecnologias modernas, o DocMark oferece uma experiência eficiente e confiável para a aplicação de carimbos digitais em documentos PDF.</p>
